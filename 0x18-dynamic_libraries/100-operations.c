#include <math.h>
#include <stdio.h>

int add(int x, int y)
{
return (x + y);
}
int sub(int x, int y)
{
return (x - y);
}
int mul (int x, int y)
{
return (x * y);
}
int div (int x, int y)
{
return (x / y);
}
int mod (int x, int y)
{
return (x % y);
}

int main(void)
{
	int a, b;

return (add(a, b));
return (sub(a, b));
return (mul(a, b));
return (div(a, b));
return (mod(a, b));
}
